local function normalize(color, fallback)
	if type(color) ~= "string" or color == "" or color == "none" then
		color = fallback
	end

	if not color then
		return "#FFFFFF"
	end

	if color:sub(1, 1) == "#" then
		return "#" .. color:sub(2):upper()
	end

	return color
end

local function build_colors(palette)
	local syntax = palette.syntax or {}
	local fg = palette.fg or {}
	local bg = palette.bg or {}
	local accent = (palette.theme and palette.theme.primary) or syntax.builtinVar or "#61AEFF"

	local function syn(key, fallback)
		return normalize(syntax[key], fallback)
	end

	return {
		bg = normalize(bg.core, "#0A0A0A"),
		body_bg = normalize(bg.shadow or bg.mantle, "#0B0F1A"),
		tile = normalize(bg.surface or bg.mantle or bg.core, "#111827"),
		fg = normalize(fg.core, "#EDE1C3"),
		accent = normalize(accent, "#61AEFF"),

		builtin_var = syn("builtinVar", "#61AEFF"),
		const = syn("constant", "#F8944D"),
		punct = syn("punctuation", "#E58383"),
		prop = syn("identifier", "#FFD393"),
		exception = syn("exception", "#D06666"),
		string = syn("string", "#53D390"),
		parameter = syn("parameter", "#C28EFF"),
		delimiter = syn("delimiter", "#D06666"),
		type = syn("type", "#81C0B6"),
		regex = syn("regex", "#96EA7F"),
		builtin_const = syn("builtinConst", "#3B9184"),
		constant = syn("constant", "#F8944D"),
		func = syn("func", "#F8B471"),
		builtin_func = syn("builtinFunc", "#E67451"),
		statement = syn("statement", "#F0E68C"),
		keyword = syn("keyword", syn("conditional", "#BDB76B")),
		special = syn("special", "#FFA247"),
		operator = syn("operator", "#FFA0A0"),
		preproc = syn("preproc", "#47E0FF"),
		bracket = syn("bracket", "#A39B8E"),
		comment = syn("comment", normalize(fg.muted or fg.dim, "#5E7B88")),
	}
end

