local function main()
	print("\n=== Oasis Social Preview Generator ===\n")

	local palette_name = arg[1] or DEFAULT_PALETTE
	local mode = arg[2] or DEFAULT_MODE

	local palette, err = utils.load_palette(palette_name, mode)
	if not palette then
		print("Error: " .. (err or "could not load palette"))
		os.exit(1)
	end

	local colors = build_colors(palette)
	local base_name = palette_name:gsub("^oasis_", "")
	local palette_label = utils.capitalize(base_name)

	local context = {
		hero_image = string.format("./images/%s-dark-code.png", base_name),
		palette_label = palette_label,
		theme_count = tostring(#utils.get_palette_names()),
	}

	for k, v in pairs(colors) do
		context[k] = v
	end

	local html = render_template(context)
	utils.write_file(OUTPUT_PATH, html)

	print(string.format("✓ Wrote %s using %s (%s)", OUTPUT_PATH, palette_name, mode))
end

main()
